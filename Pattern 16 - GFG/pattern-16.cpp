class Solution {
  public:
    void printTriangle(int n) {
        for (int i = 65; i < 65 + n; i++) {
            for (int j = 0; j <= i - 65; j++) {
                cout << char(i);
            }
            cout << endl;
        }
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        Solution ob;
        ob.printTriangle(n);
    }
    return 0;
}
