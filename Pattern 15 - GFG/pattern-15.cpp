class Solution {
  public:
    void printTriangle(int n) {
        for (int i = 65 + n - 1; i >= 65; i--) {
            for (int j = 65; j <= i; j++) {
                cout << char(j);
            }
            cout << endl;
        }
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        Solution ob;
        ob.printTriangle(n);
    }
    return 0;
}
